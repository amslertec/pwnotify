class _FakeRequest:
    """Duck-typed Request -- `oidc_callback`/`audit.record` read only these attributes."""

    def __init__(self, cookies: dict[str, str] | None = None) -> None:
        self.headers: dict[str, str] = {}
        self.client: object | None = None
        self.cookies: dict[str, str] = cookies or {}


def _extract_cookie(resp: RedirectResponse, name: str) -> str | None:
    for raw in resp.headers.getlist("set-cookie"):
        jar: SimpleCookie = SimpleCookie()
        jar.load(raw)
        if name in jar:
            return jar[name].value
    return None


def _cookie_cleared(resp: RedirectResponse, name: str) -> bool:
    for raw in resp.headers.getlist("set-cookie"):
        if raw.startswith(f"{name}=") and "Max-Age=0" in raw:
            return True
    return False


# ---- 1. No flow cookie -> rejected before the exchange is even attempted --------------- #


async def test_callback_without_flow_cookie_is_rejected(session: AsyncSession) -> None:
    request = _FakeRequest()  # no OIDC_FLOW_COOKIE set
    exchange_mock = AsyncMock()
    with patch("app.services.oidc.exchange_and_verify", new=exchange_mock):
        resp = await oidc_callback(
            request,  # type: ignore[arg-type]
            session,
            code="fake-code",
            state="some-state",
            error=None,
        )
    assert isinstance(resp, RedirectResponse)
    assert "sso_error=1" in resp.headers["location"]
    exchange_mock.assert_not_called()
    assert _extract_cookie(resp, "pwnotify_access") is None


# ---- 2. The auth redirect carries PKCE S256 + nonce; the flow cookie round-trips ------- #


def test_initiate_login_emits_pkce_and_nonce() -> None:
    auth_url, cookie = oidc.initiate_login(_SETTINGS, _REDIRECT_URI)

    assert "code_challenge=" in auth_url
    assert "code_challenge_method=S256" in auth_url
    assert "nonce=" in auth_url

    flow = oidc.decode_flow_cookie(cookie)
    assert "code_verifier" in flow
    assert "nonce" in flow
    assert "state" in flow


def test_initiate_login_requests_form_post_response_mode() -> None:
    """RFC 9700 §4.3.1: the auth-code flow must be started with `response_mode=form_post` so Entra
    returns the code in a cross-site POST body, not the redirect URL. Red against the old code,
    which passed no `response_mode`."""
    fake_flow = {"auth_uri": "https://login.example/authorize", "state": "s", "nonce": "n"}
    with patch.object(
        msal.ConfidentialClientApplication,
        "initiate_auth_code_flow",
        return_value=fake_flow,
    ) as mock_initiate:
        oidc.initiate_login(_SETTINGS, _REDIRECT_URI)

    assert mock_initiate.call_args.kwargs["response_mode"] == "form_post"


def test_flow_cookie_is_samesite_none_and_secure() -> None:
    """The flow cookie must be `SameSite=None; Secure`: with form_post the callback is a cross-site
    POST, on which a `SameSite=Lax` cookie is NOT sent, and `SameSite=None` mandates `Secure`
    (HTTPS-only SSO). Red against the old code, which set `SameSite=Lax` and `Secure` only when
    `settings.cookie_secure`."""
    resp = Response()
    set_oidc_flow_cookie(resp, "encrypted-flow-value")

    raw = next(
        h for h in resp.headers.getlist("set-cookie") if h.startswith(f"{OIDC_FLOW_COOKIE}=")
    )
    lowered = raw.lower()
    assert "samesite=none" in lowered
    assert "secure" in lowered


# ---- 3. The code_verifier MSAL generated is what MSAL receives back at the exchange ---- #


async def test_code_verifier_sent_on_exchange() -> None:
    _, cookie = oidc.initiate_login(_SETTINGS, _REDIRECT_URI)
    flow = oidc.decode_flow_cookie(cookie)

    mock_acquire = Mock(
        return_value={
            "access_token": "fake-access-token",
            "id_token_claims": {
                "preferred_username": f"alice{_TEST_DOMAIN}",
                "name": "Alice",
                "tid": _SETTINGS["graph.tenant_id"],
                "groups": [_SETTINGS["oidc.admin_group_id"]],
            },
        }
    )
    auth_response = {"code": "c", "state": flow["state"]}
    with patch.object(
        msal.ConfidentialClientApplication, "acquire_token_by_auth_code_flow", new=mock_acquire
    ):
        result = await oidc.exchange_and_verify(_SETTINGS, flow, auth_response)

    mock_acquire.assert_called_once_with(flow, auth_response)
    assert result.username == f"alice{_TEST_DOMAIN}"
    assert result.allowed is True


# ---- 4. State mismatch rejected; correct state proceeds (non-vacuous) ------------------ #


async def test_state_mismatch_rejected() -> None:
    _, cookie = oidc.initiate_login(_SETTINGS, _REDIRECT_URI)
    flow = oidc.decode_flow_cookie(cookie)

    with pytest.raises(AuthError) as exc_info:
        await oidc.exchange_and_verify(_SETTINGS, flow, {"code": "c", "state": "WRONG-STATE"})
    assert exc_info.value.code == "sso_state_mismatch"

    # Non-vacuous: the SAME flow with the CORRECT state (network mocked) proceeds.
    mock_acquire = Mock(
        return_value={
            "access_token": "fake-access-token",
            "id_token_claims": {
                "preferred_username": f"bob{_TEST_DOMAIN}",
                "tid": _SETTINGS["graph.tenant_id"],
                "groups": [_SETTINGS["oidc.admin_group_id"]],
            },
        }
    )
    with patch.object(
        msal.ConfidentialClientApplication, "acquire_token_by_auth_code_flow", new=mock_acquire
    ):
        result = await oidc.exchange_and_verify(
            _SETTINGS, flow, {"code": "c", "state": flow["state"]}
        )
    assert result.username == f"bob{_TEST_DOMAIN}"


