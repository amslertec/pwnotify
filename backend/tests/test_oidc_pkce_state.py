async def test_legit_flow_succeeds(pkce_tenant: _Env) -> None:
    auth_url, cookie = oidc.initiate_login(_SETTINGS, _REDIRECT_URI)
    assert "code_challenge=" in auth_url  # sanity: same flow the browser would have followed
    flow = oidc.decode_flow_cookie(cookie)
    request = _FakeRequest(cookies={OIDC_FLOW_COOKIE: cookie})

    mock_acquire = Mock(
        return_value={
            "access_token": "fake-access-token",
            "id_token_claims": {
                "preferred_username": f"carol{_TEST_DOMAIN}",
                "name": "Carol",
                "tid": _SETTINGS["graph.tenant_id"],
                "groups": [_SETTINGS["oidc.admin_group_id"]],
            },
        }
    )
    session_factory = get_session_factory()
    with patch.object(
        msal.ConfidentialClientApplication, "acquire_token_by_auth_code_flow", new=mock_acquire
    ):
        async with session_factory() as session:
            resp = await oidc_callback(
                request,  # type: ignore[arg-type]
                session,
                code="fake-code",
                state=flow["state"],
                error=None,
            )

    assert isinstance(resp, RedirectResponse)
    assert "sso_denied" not in resp.headers["location"]
    assert "sso_error" not in resp.headers["location"]
    assert _extract_cookie(resp, "pwnotify_access") is not None
    assert _cookie_cleared(resp, OIDC_FLOW_COOKIE), "flow cookie must be cleared on success"

    async with session_factory() as session:
        user = await user_repo.get_by_username(session, f"carol{_TEST_DOMAIN}")
        assert user is not None
        assert user.role == "admin"
