"""H7: the OIDC login must be bound to the browser (encrypted state cookie) and carry PKCE
(S256) + a nonce, using MSAL's native auth-code-flow (`initiate_auth_code_flow` /
`acquire_token_by_auth_code_flow`) instead of a handrolled, unbound `state` JWT.

Attack-oriented:
- A callback that arrives WITHOUT the browser-bound flow cookie is refused before the token
  exchange is even attempted (login-CSRF / stale or replayed callback).
- The redirect to Entra carries PKCE (`code_challenge`/`code_challenge_method=S256`) and a
  `nonce` -- both generated by MSAL itself, never touched by application code.
- The `code_verifier` MSAL generated during `initiate_login` is exactly what MSAL receives
  back at the token exchange (proves PKCE is wired end-to-end without a live Entra).
- A `state` mismatch at the exchange is rejected (`AuthError`, code `sso_state_mismatch`) --
  non-vacuous: the same flow with the CORRECT state proceeds past the check.
- A legitimate flow (cookie + matching state, network mocked) issues a session and clears the
  flow cookie.

`ConfidentialClientApplication.__init__` unconditionally performs a tenant-discovery HTTP call
(verified empirically against msal 1.37.0 -- "we always do a tenant discovery", `authority.py`;
`validate_authority=False` only skips the separate INSTANCE discovery). To keep this suite
network-independent (like `test_graph_pooling.py`/`test_graph_group_members.py`, which already
stub MSAL's network surface in this repo), `_no_network_tenant_discovery` stubs that one call;
everything else (PKCE/nonce/state generation, the real MSAL state-mismatch check) runs for real.
"""

